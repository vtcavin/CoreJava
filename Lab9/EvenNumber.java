package Lab9;
public class EvenNumber{
	
	public static void main(String[] args) {
		for(int i=0;i<=50;i=i+2) 
		{
			System.out.println(i);
		}
		int i=0;
		while(i<=50) {
			System.out.println(i);
			i=i+2;
		}
		int j=0;
		do {
			System.out.println(j);
			j=j+2;
		}
		while(j<=50);	
	}
}

